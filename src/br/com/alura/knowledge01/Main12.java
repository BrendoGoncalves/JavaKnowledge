package br.com.alura.knowledge01;

public class Main12 {
    public static void main(String [] args){
        /*
         * Estrutura de Repetição
         * DO WHILE
         */

        byte count = 0;

        do{
            System.out.println(count);
        }while(count !=0);

        do{
            System.out.println(count);
            count--;
        }while(count > -4);
    }
}
